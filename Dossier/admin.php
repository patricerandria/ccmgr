<h2>Le Règlement Intérieur</h2>
<p style="text-align:justify;">Le présent règlement intérieur régit le fonctionnement de l'Association et de ses différents organes. Il vient en
complément des statuts de l'Association et constitue un code de conduite pour ses membres.</p>
<p style="text-align:justify;">En référence à l’objet de l'Association, les associés sont animés par la volonté constante et tangible de
promouvoir la Foi, la Solidarité et la Charité auprès des catholiques en général dans un esprit de fraternité et
d’accueil.</p>
<p style="text-align:justify;">L‘Assemblée Générale définit et oriente la politique de la Fraternité Malgache; elle prend des décisions au nom
de l’association en tenant toujours compte des avis et considérations des différents organes. Le Conseil
d’Administration gère quotidiennement le fonctionnement de l’association.</p>
<p style="text-align:justify;">La Communauté Catholique Malgache de Grenoble est une association catholique; de ce fait, tout acte posé
au sein de celle-ci doit cadrer avec la doctrine catholique. Tout membre est tenu de respecter l’éthique
catholique qui est d’application au sein de l’association.</p>
<p style="text-align:justify;">De par son objet (article 2 des statuts), la Communauté Catholique Malgache de Grenoble se veut
multiculturelle et s’inspire essentiellement des directives de la Commission Episcopale en tant que
Communauté Catholique d’origine étrangère en France. Elle fait partie de la Pastorale des Migrants et de l’
Aumônerie Nationale des Catholiques Malgaches de France.</p>
<p style="text-align:justify;">Les membres de l’association vivent en France depuis plusieurs années ou sont de passage pour leurs
études. Ils éprouvent parfois le besoin d’exprimer leur foi en malgache, et selon leur coutume. C’est de là
que la Communauté Catholique Malgache de Grenoble s’est constituée et célèbre des offices, en plus des activités spécifiques.</p>
