<h3>1987 :</h3>
<p style="text-align:justify;"> 
C'est en 1987 qu'une poignée de familles catholiques de l'agglomération grenobloise décident de se
rassembler pour former une communauté catholique de rencontre et de prière entre malgaches et
amis de Madagascar.<br/><br/>
Avec l'appui du diocèse, l'Evêque de Grenoble nomme alors le Père François Garrec, ayant été
auparavant missionnaire de la Salette à Madagascar, comme prêtre aumônier de la Communauté.
<br/><br/>Le Relais Notre-Dame Réconciliatrice accueille généreusement la Communauté dans son oratoire pour célébrer tous les 3èmes dimanches de chaque mois une messe en malgache.
<br/><br/>Les Soeurs de la Salette sis rue Joseph Fourier participaient activement à la liturgie et à l'animation des messes de la communauté avec l'appui de leur propre livret de chants.
<br/><br/>La Communauté Catholique Malgache de Grenoble était née en prenant l'appelation "Fihavanana".
</p>
<h3>1987-1997 :</h3>
<p style="text-align:justify;">
La Communauté décide de faire partie de l'Aumônerie Nationale des Catholiques Malgaches de France (précédemment U.F.C.C.M.F).
<br/><br/>La Communauté édite son propre livret de chants et participe avec les jeunes issus de l'immigration
des différentes communautés de Grenoble au premier rassemblement des "Jeunes Pentecôte 97" à
Lourdes en rejoignant les communautés de autres villes de France.
</p>
<h3>1997-2000 :</h3>
<p style="text-align:justify;">
En 1998, La Communauté accueille le Congrès Régional des Communautés Catholiques Malgaches de la zone Est & Sud en recevant les communautés de Lyon, Marseille, Strasbourg et Annemasse à La Salette.
<br/><br/>En 1999, les jeunes de la Communauté participent au Rassemblement régional des Jeunes Pentecôte (JP'99) de la région Centre-Est à La Salette.
</p>
<h3>2000-2004 :</h3>
<p style="text-align:justify;">
En 2002, la Communauté décide la création d'un bureau permanent tout en maintenant la gestion collégiale et prend l'initiative de créer un groupe de jeunes pour favoriser l'intégration des nouveaux étudiants en provenance de Madagascar et des jeunes issus de familles malgaches nés en France.
<br/><br/>En 2003, le Groupe des Jeunes édite un nouveau livret avec l'addition de quelques cantiques contemporains.
<br/><br/>Le Groupe des Jeunes participe pour la première fois au Congrès National des Jeunes à Reyssins (42).
</p>
