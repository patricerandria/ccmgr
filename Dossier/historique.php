<h3>LA COMMUNAUTE:</h3>
<br/>
<h2>Une communauté de prière</h2>
Nous nous retrouvons surtout pour prier ensemble avec la célébration d'une messe en malgache
tous les 3èmes dimanche de chaque mois à 10H30 de Septembre à Juin. Notre démarche s'inscrit
dans la confirmation et l'approfondissement de notre foi chrétienne et catholique en respectant
l'héritage de notre inculturation.
<br/><br/>
<h2>Une communauté d'esprit et de partage</h2>
Nous nous retrouvons ainsi souvent pour partager un repas ou un apéritif ensemble, organiser des sorties, pique-nique ou pélérinages et en certaines occasions festives (Noël, Pâques, célébrations de baptême, communion, mariage ... ) avec toujours l'ambition de cultiver l'esprit de notre propre culture et du "fihavanana" : solidarité, amitié, fraternité, dignité,
<br/><br/>
<h2>Une communauté d'ouverture</h2>
Nous participons en compagnie des autres communautés issues de l'immigration à Grenoble
(italienne, polonaise, africaine, espagnole, portugaise, laotienne, cambodgienne, ...) aux activités
de la Pastorale des Migrants pour témoigner de notre intégration au sein du Diocèse de Grenoble
et de l'Eglise Catholique en France et pour promouvoir l'esprit du "vivre ensemble",
Nous ne manquons pas de répondre aux invitations des paroisses qui sollicitent notre contribution pour animer des célébrations de chants ou de processions malgaches.
<br/><br/>
<h2>Une communauté d'écoute</h2>
Il ne faut pas oublier aussi qu'il revient à nous tous laïcs ou religieux de faire vivre notre communauté alors n'hésitons pas à venir voir et faire part de nos propres initiatives ou idées pour les concrétiser ensemble.



