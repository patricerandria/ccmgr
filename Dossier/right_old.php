<li id="active" style="text-align:justify;">Spectacle des enfants</li>
<li id="active" style="text-align:justify;">Distribution des papillotes</li>
<li id="active" style="text-align:justify;">Scène ouverte pour tous (chants, danses, etc…)</li>
