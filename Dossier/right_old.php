<h1 class="right" >Messe de Noël</h1>
<br/>
<p style="text-align:justify;">La Communauté Catholique Malgache de Grenoble fêtera Noël avec un peu d’avance, le dimanche 16 Décembre 2012 à 10h à la Chapelle St Maurice (St Martin d’Hères).
Tous les enfants du catéchisme et de l’éveil à la foi  animeront la liturgie. Ils vous préparent  une belle célébration avec des chants chaleureux et des danses de louange. Nous comptons sur votre présence.
Vivons cette messe  comme un temps de partage, une journée magique, pleine d’espoir, de joie malgré les difficultés de la vie.</p>
<p style="text-align:justify;">A la fin de la messe, l’équipe de la catéchèse organisera un <strong>Vokatra</strong>, dont le but sera de financer les activités de l’année 2012/2013 (sorties, matériels, etc…) N’hésitez pas à être généreux.
Comme chaque année, chacun pourra apporter un repas tiré du sac qu’on mettra en commun pour continuer les festivités.</p>
<p style="text-align:justify;"><strong><u>Dans l’après-midi:</u></strong></p>
