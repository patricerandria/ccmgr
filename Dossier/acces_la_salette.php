<h4 style="text-align:justify;">Par la route :</h4>
<p style="text-align:justify;">
<strong> En voiture :<br/><br/> </strong>

Depuis Grenoble (pour les personnes venant du nord), prendre la RN 85 (dite ’Route Napoléon’) et en passant par Vizille et La Mure, arriver à Corps. Au village de Corps prendre à gauche la RD 212c en direction de Notre Dame de La Salette. Grenoble - La Salette : 80 Km ; compter à peu près 1H30.
<br/><br/>
Depuis Gap (pour les personnes venant du sud), prendre la RN 85 (dite ’Route Napoléon’) jusqu’à Corps. Au village de Corps prendre à droite la RD 212c en direction de Notre Dame de La Salette. Gap - La Salette : 60 Km ; compter à peu près 1H.
</p>

<p style="text-align:justify;">
<strong>En bus :<br/><br/> </strong>

Des liaisons régulières par car relient les villes de Grenoble et de Gap avec Corps. En juillet et août, le car venant de Grenoble monte jusqu’au Sanctuaire. Un service de taxis assure la liaison entre Corps et le Sanctuaire durant toute l’année.
<br/>
Vous pouvez trouver les fiches horaires et votre itinéraire selon la période sur le site Internet de TransIsere. N’oubliez pas d’indiquer votre point d’arrivée : ’La Salette - Sanctuaire’ ou ’Corps’.
<br/>
